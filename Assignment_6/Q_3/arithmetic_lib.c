int Addition(int, int);
int Substraction(int, int);
int Multiplication(int, int);
int Division(int, int);

int Addition(int A, int B)
{
    return A + B;
}
int Substraction(int A, int B)
{
    return A - B;
}
int Multiplication(int A, int B)
{
    return A * B;
}
int Division(int A, int B)
{
    return (A / B);
}
